"""
Na matematica existe uma regra que indica quais operacoes devem ser executdas primeiro. 
Isso é util pois ao analisar uma expressão, a depender da ordem das operacoes o valor pode ser diferente

A definição segue a seguinte ordem como correta:

1. Parentesis
2. Expoentes
3. Multiplicao e divisao (da esquerda pra direita)
4. soma e subtracao (da esquerda pra direita)

Exemplos:
"""
print(10 - 5 * 2)

print((10 - 5) * 2)

print(10 ** 2 * 2)

print(10 ** (2 * 2))

